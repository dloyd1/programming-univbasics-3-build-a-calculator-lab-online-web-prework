def addition(num1, num2)
  num1 + num2
end

def subtraction(num1, num2)
  num1 - num2
end

def division(num1, num2)
  value_div = num1 / num2
  value_div
end

def multiplication(num1, num2)
  value_prod = num1 * num2
  value_prod
end

def modulo(num1, num2)
  value_mod = num1.remainder(num2)
  value_mod
end

def square_root(num)
  value_sqrt = Math.sqrt(num)
  value_sqrt
end

